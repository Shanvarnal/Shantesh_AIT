// area of triangle
var l=parseInt(prompt("Enter base of triangle:"))
var h=parseInt(prompt("Enter height of triangle"))

document.write("Area of Triangle="+0.5*l*h+"<br>")

// area of rectangle
var l=parseInt(prompt("Enter length of rectangle:"))
var h=parseInt(prompt("Enter width of rectangle"))

document.write("Area of rectangle="+l*h+"<br>")

//area of circle
var r=parseInt(prompt("Enter radius of circle"))

const pi=3.14
document.write("Area of Circle="+Math.round(pi*r*r)+"<br>")

//area of square
var s=parseInt(prompt("Enter side of square:"))

document.write("Area of Square="+s*s+"<br>")

